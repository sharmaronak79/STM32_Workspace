/*
 * @brief Read Temperature
 * retval None
 * */
void Read_Temp(void){


	//uint8_t buf[20];
	buf[0]=Temp_REG;
	uint8_t low_byte;
	uint8_t high_byte;
	ret = HAL_I2C_Master_Transmit(&hi2c2,TMP1407_ADDR, buf, 1, 100);


	if(ret != HAL_OK){
		strcpy((char*)buf,"Error Tx \r\n");
	}else{
		// Read two bytes from the temperature register or Slave register
		ret = HAL_I2C_Master_Receive(&hi2c2, TMP1407_ADDR, buf, 2, 100);

		if(ret != HAL_OK){
			strcpy((char*)buf,"Error Rx \r\n");
		}else{

			//Combine the bytes
			uint16_t reading;
			reading = buf[0]<<8;
			reading = reading | buf[1];



			//Print I2C receive Data in Hex format
			low_byte=buf[0];
			high_byte=buf[1];
			sprintf((char*)buf,"Received data from I2C : %X\n",low_byte);
			HAL_UART_Transmit(&huart3, buf, strlen((char*)buf), 100);
			sprintf((char*)buf,"Received data from I2C : %X\n",high_byte);
			HAL_UART_Transmit(&huart3, buf, strlen((char*)buf), 100);

			//Convert to Temperature
			Temp = reading * 0.0078;
			sprintf((char*)buf,"Temp = %0.3f C \r",Temp);

		}
	}

	HAL_UART_Transmit(&huart3, buf, strlen((char*)buf), 100);
	HAL_Delay(1000);

}

/*
 * @brief Read RPM from Fan controller EMC2301
 * retval None
 */
void Read_RPM(void){
	static const uint8_t FAN_ADDR = 0x2F<<1;
	uint8_t read_TACH_High_Reg = 0x3E;
	uint8_t read_TACH_Low_Reg = 0x3F;
	float RPM;
	uint8_t val_3E=0;
	uint8_t val_3F=0;
	uint16_t val;

	HAL_I2C_Master_Transmit(&hi2c2, FAN_ADDR, &read_TACH_High_Reg, 1, 100);
	if(ret != HAL_OK){
		strcpy((char*)buf,"Error in Tx for 3E Reg\n");
	}else{
		HAL_I2C_Master_Receive(&hi2c2, FAN_ADDR, &val_3E, 1, 100);
		val = val_3E;
		val= val<<8;
	}

	HAL_I2C_Master_Transmit(&hi2c2, FAN_ADDR, &read_TACH_Low_Reg, 1, 100);
	if(ret != HAL_OK){
		strcpy((char*)buf,"Error in Tx for 3F Reg\n");
	}else{
		HAL_I2C_Master_Receive(&hi2c2, FAN_ADDR, &val_3F, 1, 100);
		val= val | val_3F;
		val = val>>3;
	}

	RPM = 3932160 / val;
	sprintf((char*)buf,"Actual RPM : %0.1f \n",RPM);

	HAL_UART_Transmit(&huart3, buf, strlen((char*)buf), 100);

}


